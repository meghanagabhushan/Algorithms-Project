public class EmergencyVehicle {
    int vehicleID;
    int vehicleType;
    int zipCode;
    int availability;

    public EmergencyVehicle(int vehicleID, int vehicleType, int zipCode, int availability) {
        this.vehicleID = vehicleID;
        this.vehicleType = vehicleType;
        this.zipCode = zipCode;
        this.availability = availability;
    }
}
